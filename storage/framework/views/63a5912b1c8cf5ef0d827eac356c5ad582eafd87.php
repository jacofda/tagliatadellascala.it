        	<h4 class="mt-0 font-alt">Perché nasce la Scala dei sapori?</h4>
        	<p>Da diversi anni le associazioni locali si ritrovano con l’intento di valorizzare il territorio e di trovare una soluzione al problema della carenza di volontari. Ogni associazione opera con una decina di persone, a loro volta inserite in altri sodalizi, che nel corso della realizzazione dei diversi eventi, diminuiscono. Per quanto riguarda la valorizzazione del territorio, le associazioni si rendono conto che vi è una diffusa scarsa conoscenza di luoghi, attività, prodotti presenti  sul territorio da parte delle persone che ci vivono. Manca quindi una vetrina interattiva che raccolga in modo innovativo le voci nascoste e di qualità che campeggiano in zona.</p>
        	<p>Partendo da queste considerazioni è nato dunque il comitato organizzativo che si compone di circa otto associazioni operanti in loco, ognuna portatrice  didiverse sensibilità e conoscenze, che si muovono in sinergia per <b>dare una risposta alla preservazione ecrescita del territorio</b>. Ogni sodalizio ha inoltre  contatti con molteplici altri gruppi di volontari e simpatizzanti, consentendo di mobilitare per l’evento oltre una sessantina di persone, fra le quali una  quindicina si preoccupano anche della pulizia della fortificazione della Tagliata della Scala prima e dopo la realizzazione della manifestazione.</p>
        	<p>Il volano che si innesca è virtuoso: un ampio numero di volontari comporta un ridotto dispendio di energie e, allo stesso tempo, la poca conoscenza delle opportunità che offre il territorio, viene spazzato via da una vetrina mediatica che non ha eguali nella valle.</p>


