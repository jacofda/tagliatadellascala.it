        	<h4 class="mt-0 font-alt">Le prossime edizioni</h4>
        	<p>Il <b>comitato organizzativo della Scala dei sapori</b> opera perseguendo l’obiettivo della vetrina interattiva del territorio, ma  ridefinendo annualmente le finalità della singola edizione, in modo da non precludersi a nuove idee, proposte, nuovi gruppi e  persone che desiderino operare attivamente all’evento. In questa vetrina sono sicuramente presenti i prodotti tipici e le ricette della tradizione, ma viene dato spazio alla presentazione dei siti di  interesse storico, artistico culturale di Canal di Brenta e Feltrino, e in un secondo tempo all’Altopiano dei 7 Comuni e della Valsugana.  Il comitato lavora per inserire la manifestazione in diversi percorsi già esistenti, tour di degustazioni (anche con il supporto  dei ristoranti) per farla conoscere progressivamente allo slow food e al mondo dei km zero, inoltre si impegna per dare vita a degli  eventi satellite, come ad esempio una castagnata in dicembre nel forte e alcuni concerti.  Per arricchire la passeggiata <b>sono presenti laboratori didattici e attività per i bambini</b>, nonché iniziative culturali di vario genere,  inclusi incontri con l’autore e <b>visite guidate alla fortificazione</b>, rivelando ai locali e ai visitatori quali eccellenze insistano nel territorio, tutte da visitare, sperimentare e assaporare.</p>

